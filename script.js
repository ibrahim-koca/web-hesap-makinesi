// Tema y√∂netimi i√ßin daha mod√ºler bir yapƒ±
const themeManager = {
  isDarkMode: window.matchMedia('(prefers-color-scheme: dark)').matches,
  
  init() {
    const savedMode = localStorage.getItem('darkMode');
    if (savedMode !== null) this.isDarkMode = savedMode === 'true';
    this.apply();
    
    // Sistem tema deƒüi≈üikliƒüini dinle
    window.matchMedia('(prefers-color-scheme: dark)').addEventListener('change', e => {
      if (!localStorage.getItem('darkMode')) {
        this.isDarkMode = e.matches;
        this.apply();
      }
    });
    
    // Toggle butonu
    elements.modeToggle.addEventListener("click", () => {
      this.toggle();
    });
  },
  
  apply() {
    elements.body.classList.toggle("dark-mode", this.isDarkMode);
    elements.modeToggle.textContent = this.isDarkMode ? "üåô" : "‚òÄÔ∏è";
  },
  
  toggle() {
    this.isDarkMode = !this.isDarkMode;
    localStorage.setItem('darkMode', this.isDarkMode);
    this.apply();
  }
};

// Sayƒ± formatlamasƒ± i√ßin singleton nesne
const formatter = {
  // Bin ayƒ±rƒ±cƒ± i√ßin format fonksiyonu
  format(num) {
    if (num === "" || num === undefined) return num;
    
    // Negatif sayƒ± kontrol√º
    let isNegative = false;
    let numStr = num.toString();
    
    // Eƒüer sayƒ± negatifse, negatif i≈üaretini kaldƒ±r (format ederken)
    if (numStr.startsWith('-')) {
      isNegative = true;
      numStr = numStr.substring(1);
    }
    
    // Ondalƒ±k kƒ±smƒ±nƒ± ve tam sayƒ± kƒ±smƒ±nƒ± ayƒ±r
    const parts = numStr.split('.');
    
    // Tam sayƒ± kƒ±smƒ±nƒ± formatla (bin ayƒ±rƒ±cƒ± ekle)
    parts[0] = parts[0].replace(/\B(?=(\d{3})+(?!\d))/g, ".");
    
    // Tekrar birle≈ütir
    let result = parts.join(',');
    
    // Eƒüer sayƒ± negatifse, parantez i√ßinde g√∂ster
    if (isNegative) {
      result = "(-" + result + ")";
    }
    
    return result;
  },
  
  // Formatƒ± kaldƒ±r
  unformat(str) {
    if (typeof str !== 'string') return str;
    
    // Parantezli negatif formattan normal negatif formata d√∂n√º≈üt√ºr
    if (str.startsWith('(-') && str.endsWith(')')) {
      str = '-' + str.substring(2, str.length - 1);
    }
    
    return str.replace(/\./g, '').replace(',', '.');
  }
};

// Klavye rehberi y√∂netimi
const keyboardGuideManager = {
  isVisible: false,
  
  init() {
    elements.keyboardGuideToggle.addEventListener('click', () => {
      this.toggle();
    });
    
    // Rehberi gizle/g√∂ster i≈ülevi i√ßin olay dinleyicisi
    document.addEventListener('click', (e) => {
      // Eƒüer rehber g√∂r√ºn√ºr ve tƒ±klanan element rehber veya toggle butonu deƒüilse rehberi kapat
      if (this.isVisible && 
          e.target !== elements.keyboardGuide && 
          !elements.keyboardGuide.contains(e.target) &&
          e.target !== elements.keyboardGuideToggle) {
        this.hide();
      }
    });
  },
  
  show() {
    elements.keyboardGuide.classList.add('visible');
    this.isVisible = true;
    elements.keyboardGuideToggle.classList.add('active');
  },
  
  hide() {
    elements.keyboardGuide.classList.remove('visible');
    this.isVisible = false;
    elements.keyboardGuideToggle.classList.remove('active');
  },
  
  toggle() {
    if (this.isVisible) {
      this.hide();
    } else {
      this.show();
    }
  }
};

// Hesap makinesi i≈ülevselliƒüi i√ßin daha yapƒ±landƒ±rƒ±lmƒ±≈ü bir nesne
const calculator = {
  init() {
    // Event dinleyicilerini ekle
    this.setupEventListeners();
  },
  
  setupEventListeners() {
    // Buton tƒ±klamalarƒ± i√ßin event delegation
    elements.buttons.addEventListener('click', this.handleButtonClick.bind(this));
    
    // Klavye desteƒüi
    document.addEventListener("keydown", this.handleKeyPress.bind(this));
  },
  
  handleButtonClick(e) {
    if (e.target.tagName !== 'BUTTON') return;
    
    const action = e.target.textContent;
    this.processAction(action);
  },
  
  handleKeyPress(event) {
    const key = event.key;
    
    this.clearIfError();
    
    // Sayƒ± tu≈ülarƒ±
    if (key >= '0' && key <= '9') {
      event.preventDefault();
      this.appendNumber(key);
      return;
    }
    
    // √ñzel tu≈ülar i√ßin switch daha verimli
    switch (key) {
      case '+':
      case '-':
      case '*':
      case '/':
        event.preventDefault();
        this.appendOperator(key);
        break;
        
      case '.':
      case ',':
        event.preventDefault();
        this.appendDecimal();
        break;
        
      case 'Enter':
        event.preventDefault();
        this.calculate();
        break;
        
      case 'Backspace':
        event.preventDefault();
        this.deleteLastCharacter();
        break;
        
      case '%':
        event.preventDefault();
        this.calculatePercentage();
        break;
        
      default:
        // Alt tu≈üu kombinasyonlarƒ±
        if (event.altKey) {
          event.preventDefault();
          
          const keyCode = event.keyCode || event.which;
          
          if (keyCode === 67 || keyCode === 99) { // Alt + C (b√ºy√ºk/k√º√ß√ºk c)
            this.clearResult();
          }
          else if (keyCode === 78 || keyCode === 110) { // Alt + N (b√ºy√ºk/k√º√ß√ºk n)
            this.toggleNegative();
          }
        }
        break;
    }
  },
  
  processAction(action) {
    // Eylem tipine g√∂re branch yap
    switch (action) {
      case '=': 
        this.calculate();
        break;
      case '‚Üê': 
        this.deleteLastCharacter();
        break;
      case 'C': 
        this.clearResult();
        break;
      case '.': 
        this.appendDecimal();
        break;
      case '%':
        this.calculatePercentage();
        break;
      case '+/-':
        this.toggleNegative();
        break;
      default:
        // Operat√∂r m√º?
        if (['+', '-', '*', '/'].includes(action)) {
          this.appendOperator(action);
        }
        // Sayƒ± mƒ±?
        else if (!isNaN(action)) {
          this.appendNumber(action);
        }
        break;
    }
  },
  
  clearIfError() {
    const resultValue = elements.result.value;
    
    if (
      resultValue === "Hata" || 
      resultValue === "undefined" || 
      resultValue.startsWith("Hata:")
    ) {
      elements.result.value = "";
      elements.expression.textContent = "";
      elements.result.classList.remove("error");
    }
  },
  
  appendNumber(n) {
    this.clearIfError();
    elements.result.value += n;
    
    // Sayƒ±larƒ± bin ayƒ±rƒ±cƒ± ile g√∂ster - yalnƒ±zca gerekirse yeniden formatla
    const unformatted = formatter.unformat(elements.result.value);
    if (!isNaN(unformatted)) {
      elements.result.value = formatter.format(unformatted);
    }
  },
  
  appendOperator(op) {
    this.clearIfError();
    
    // Eƒüer deƒüer bo≈üsa ve "-" operat√∂r√º girildiyse negatif sayƒ± olarak kabul et
    if (elements.result.value === "" && op === "-") {
      elements.result.value = "-";
      return;
    }
    
    const lastChar = elements.result.value.slice(-1);
    
    // Operat√∂rden sonra sayƒ± olmadan tekrar operat√∂r giri≈üi yapƒ±lmasƒ±nƒ± engelle
    if (['+', '-', '*', '/', '.'].includes(lastChar) || elements.result.value === "") return;
    
    // Operat√∂r ekleniyor
    elements.result.value += op;
  },
  
  appendDecimal() {
    this.clearIfError();
    
    // Son i≈ülem ondalƒ±k i√ßeriyorsa tekrar ondalƒ±k ekleme
    const unformatted = formatter.unformat(elements.result.value);
    const parts = unformatted.split(/[+\-*/]/);
    const lastPart = parts[parts.length - 1];
    
    if (lastPart.includes('.')) {
      this.showError("Ge√ßersiz ondalƒ±k!");
      return;
    }
    
    if (elements.result.value === "") {
      elements.result.value = "0.";
    } else {
      const lastChar = unformatted.slice(-1);
      if (['+', '-', '*', '/'].includes(lastChar)) {
        elements.result.value += "0.";
      } else {
        elements.result.value = formatter.format(unformatted + (lastPart === "" ? "0." : "."));
      }
    }
  },
  
  deleteLastCharacter() {
    // Eƒüer formatlƒ± bir sayƒ± varsa, formatƒ± kaldƒ±r, karakteri sil, tekrar formatla
    const unformatted = formatter.unformat(elements.result.value);
    const newValue = unformatted.slice(0, -1);
    
    // Eƒüer sadece sayƒ± varsa ve sayƒ±sal bir deƒüerse formatla
    if (!isNaN(newValue) && newValue !== "") {
      elements.result.value = formatter.format(newValue);
    } else {
      elements.result.value = newValue;
    }
  },
  
  clearResult() {
    elements.result.value = "";
    elements.expression.textContent = "";
    elements.result.classList.remove("error");
  },
  
  showError(message) {
    elements.result.value = "Hata: " + message;
    elements.result.classList.add("error");
    
    // setTimeout yerine requestAnimationFrame kullanarak daha verimli temizleme
    const timeoutId = setTimeout(() => {
      elements.result.value = "";
      elements.result.classList.remove("error");
      clearTimeout(timeoutId);
    }, 2000);
  },
  
  calculate() {
    if (!elements.result.value) return;
    
    try {
      // Formatlƒ± g√∂sterimi hesaplama i√ßin d√ºzelt
      const unformatted = formatter.unformat(elements.result.value);
      const sanitizedInput = unformatted.replace(/[^0-9+\-*/().]/g, '');
      
      // Mevcut ifadeyi ifade alanƒ±na ekle
      elements.expression.textContent = elements.result.value + " = ";
      
      // Sƒ±fƒ±ra b√∂lme kontrol√º - regex ile daha hƒ±zlƒ±
      if (/\/\s*0(?!\d)/.test(sanitizedInput)) {
        this.showError("Sƒ±fƒ±ra b√∂lme hatasƒ±!");
        return;
      }
      
      // Function kullanƒ±mƒ± yerine daha g√ºvenli bir eval y√∂ntemi
      const result = evaluateExpression(sanitizedInput);
      
      if (result === Infinity || result === -Infinity) {
        this.showError("Sƒ±fƒ±ra b√∂lme hatasƒ±!");
        return;
      }
      
      if (isNaN(result)) {
        this.showError("Ge√ßersiz i≈ülem!");
        return;
      }
      
      elements.result.value = formatter.format(result);
      
    } catch (error) {
      this.showError("Hesaplama hatasƒ±!");
    }
  },
  
  // YENƒ∞: Y√ºzde hesaplama fonksiyonu
  calculatePercentage() {
    if (!elements.result.value) return;
    
    try {
      // ƒ∞fadeyi analiz et
      const unformatted = formatter.unformat(elements.result.value);
      
      // ƒ∞fadeyi matematiksel sembollerle par√ßalara ayƒ±r
      const parts = unformatted.split(/([+\-*/])/);
      
      // Eƒüer tek sayƒ± varsa basit√ße /100 yapalƒ±m
      if (parts.length === 1) {
        const value = parseFloat(parts[0]);
        elements.result.value = formatter.format(value / 100);
        return;
      }
      
      // Son sayƒ±yƒ± ve operat√∂r√º al
      const lastNumber = parts[parts.length - 1];
      const operator = parts[parts.length - 2];
      const previousNumber = parts[parts.length - 3];
      
      // Eƒüer son i≈ülemde sayƒ± yoksa hata g√∂ster
      if (lastNumber === "" || isNaN(lastNumber)) {
        this.showError("Ge√ßersiz y√ºzde i≈ülemi!");
        return;
      }
      
      let result;
      switch (operator) {
        case '+':
        case '-':
          // Toplama/√ßƒ±karma i√ßin: sayƒ±1 +/- (sayƒ±1 * sayƒ±2 / 100)
          result = parseFloat(previousNumber) + (parseFloat(previousNumber) * parseFloat(lastNumber) / 100 * (operator === '+' ? 1 : -1));
          break;
        case '*':
          // √áarpma i√ßin: sayƒ±1 * (sayƒ±2 / 100)
          result = parseFloat(previousNumber) * (parseFloat(lastNumber) / 100);
          break;
        case '/':
          // B√∂lme i√ßin: sayƒ±1 / (sayƒ±2 / 100)
          result = parseFloat(previousNumber) / (parseFloat(lastNumber) / 100);
          break;
        default:
          // Eƒüer operat√∂r yoksa, sadece sayƒ±yƒ± 100'e b√∂l
          result = parseFloat(lastNumber) / 100;
      }
      
      // Sonucu g√∂ster
      elements.result.value = formatter.format(result);
      
    } catch (error) {
      this.showError("Y√ºzde hesaplama hatasƒ±!");
    }
  },
  
  // D√úZELTILMI≈û: Negatif/Pozitif deƒüi≈ütirme fonksiyonu
  toggleNegative() {
    if (!elements.result.value) return;
    
    try {
      // Deƒüeri al
      let currentValue = elements.result.value;
      let unformatted = formatter.unformat(currentValue);
      
      // Negatif deƒüilse negatif yap, negatifse pozitif yap
      if (currentValue.startsWith('(-') && currentValue.endsWith(')')) {
        // Parantez i√ßindeki negatif sayƒ±yƒ± pozitif yap
        unformatted = unformatted.substring(1); // Ba≈üƒ±ndaki - i≈üaretini kaldƒ±r
        elements.result.value = formatter.format(parseFloat(unformatted));
      } else {
        // Operat√∂r i√ßeren ifade mi kontrol et
        const operators = ['+', '-', '*', '/'];
        let lastOperatorPos = -1;
        
        for (const op of operators) {
          const pos = unformatted.lastIndexOf(op, unformatted.length - 2); // Son sayƒ±da olabilecek - i≈üaretini atla
          if (pos > lastOperatorPos) {
            lastOperatorPos = pos;
          }
        }
        
        if (lastOperatorPos === -1) {
          // Tek sayƒ± var, negatif yap
          const num = parseFloat(unformatted);
          elements.result.value = formatter.format(-num);
        } else {
          // ƒ∞≈ülem var, son sayƒ±yƒ± bul ve negatif/pozitif yap
          const beforePart = unformatted.substring(0, lastOperatorPos + 1);
          let lastPart = unformatted.substring(lastOperatorPos + 1);
          
          if (lastPart.startsWith('-')) {
            // Zaten negatifse pozitif yap
            lastPart = lastPart.substring(1);
          } else {
            // Pozitifse negatif yap
            lastPart = '-' + lastPart;
          }
          
          // Birle≈ütir ve formatla
          const newValue = beforePart + lastPart;
          elements.result.value = newValue; // Burada direkt birle≈ütirip koyuyoruz, format sonrasƒ± g√∂r√ºlecek
          
          // Hesap makinesindeki g√∂sterimi g√ºncellemek i√ßin sayƒ± tu≈üu gibi davranarak format uygulayalƒ±m
          this.appendNumber('');
        }
      }
    } catch (error) {
      this.showError("ƒ∞≈ülem hatasƒ±!");
    }
  }
};

// G√ºvenli eval fonksiyonu
function evaluateExpression(expr) {
  try {
    // Hala new Function kullanƒ±yoruz ancak daha g√ºvenli bir ≈üekilde
    return new Function(`'use strict'; return (${expr})`)();
  } catch (e) {
    throw new Error("Hesaplama hatasƒ±");
  }
}

